    private void crearTabs(List<String> disc,JPanel p,JTabbedPane pane)
    {
        int n = disc.size();
        rellenar(n,p,disc);
        n-=10;
        int i = 2;
        while(n>0)
        {
            JPanel aux = new JPanel();
            pane.add(aux,Integer.toString(i));
            rellenar(n,aux,disc);
            n-=10;
            i++;
        }
        index = 0;
    }
    private void rellenar(int n,JPanel p,List<String> disc)
    {
        Lista_ID = db.getTopicsID(1);
        p.setLayout(new BoxLayout(p,BoxLayout.Y_AXIS));
        for(int i = 0;i<Math.min(n, 10);i++)
        {
        int aux = Integer.parseInt(Lista_ID.get(index));
        p.add(new DiscusionButton(disc.get(i),this,db,usuario,aux));
        index++;
        }
    }
